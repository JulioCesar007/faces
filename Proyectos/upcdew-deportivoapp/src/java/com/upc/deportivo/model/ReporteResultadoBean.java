package com.upc.deportivo.model;

/**
 *
 * @author noemi
 */
public class ReporteResultadoBean {

    private double empate;
    private double ganados;
    private double perdidos;



    /**
     * @return the empate
     */
    public double getEmpate() {
        return empate;
    }

    /**
     * @param empate the empate to set
     */
    public void setEmpate(double empate) {
        this.empate = empate;
    }

    /**
     * @return the ganados
     */
    public double getGanados() {
        return ganados;
    }

    /**
     * @param ganados the ganados to set
     */
    public void setGanados(double ganados) {
        this.ganados = ganados;
    }

    /**
     * @return the perdidos
     */
    public double getPerdidos() {
        return perdidos;
    }

    /**
     * @param perdidos the perdidos to set
     */
    public void setPerdidos(double perdidos) {
        this.perdidos = perdidos;
    }

}

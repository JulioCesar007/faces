package pe.joedayz.ejemplos.cinedayz.dominio;

public class Local {
	String localCodigo;
	String localNombre;
	String localDireccion;
	String localManager;
	
	public Local()
	{
		
	}
	public String getLocalCodigo() {
		return localCodigo;
	}
	public void setLocalCodigo(String localCodigo) {
		this.localCodigo = localCodigo;
	}
	public String getLocalNombre() {
		return localNombre;
	}
	public void setLocalNombre(String localNombre) {
		this.localNombre = localNombre;
	}
	public String getLocalDireccion() {
		return localDireccion;
	}
	public void setLocalDireccion(String localDireccion) {
		this.localDireccion = localDireccion;
	}
	public String getLocalManager() {
		return localManager;
	}
	public void setLocalManager(String localManager) {
		this.localManager = localManager;
	}
	
}
